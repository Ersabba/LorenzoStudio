package com.ericsson.mgre.exception;


public class CurvaCaricoConfigException extends Exception {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public CurvaCaricoConfigException() {
		super();
	}

	public CurvaCaricoConfigException(String arg0) {
		super(arg0);
	}

	public CurvaCaricoConfigException(Throwable arg0) {
		super(arg0);
	}

	public CurvaCaricoConfigException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}
